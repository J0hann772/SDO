# Перебираем числа N начиная с 1
for n in range(1, 100):
    # 1. Получаем двоичную запись числа N
    s = bin(n)[2:]

    # 2. Дописываем остаток от суммы цифр на 2 (дважды)
    # Сумма цифр в двоичной системе - это количество единиц
    for i in range(2):
        sm = s.count('1')
        s = s + str(sm % 2)

    # Переводим полученную строку обратно в число
    r = int(s, 2)

    # Проверяем условие задачи
    if r > 85:
        print(n)
        break